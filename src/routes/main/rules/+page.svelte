<div class="rules-page">
    <h1>Kites Community Rules</h1>
    <p class="introduction">Welcome to the Kites Community! These rules are in place to ensure a positive and respectful environment for everyone. Please take a moment to read them carefully.</p>

    <div class="rule-category">
        <h2>Respectful Conduct</h2>
        <ul>
        <li>Be respectful to all users. Treat everyone with kindness and courtesy.</li>
        <li>No personal attacks, harassment, or doxxing. Focus on the content, not the person.</li>
        <li>Avoid hate speech, discrimination, or any language that demeans individuals or groups based on their identity.</li>
        </ul>
    </div>

    <div class="rule-category">
        <h2>Content Guidelines</h2>
        <ul>
        <li>No spam or excessive self-promotion. Contribute meaningfully to the community.</li>
        <li>Do not spread misinformation or engage in deliberate deception.</li>
        <li>Respect copyright and intellectual property. Do not post content that you do not have the right to share.</li>
        </ul>
    </div>

    <div class="rule-category">
        <h2>Posting Guidelines</h2>
        <ul>
        <li>Use clear and descriptive titles for your posts.</li>
        <li>Flair your posts appropriately to help others understand the content.</li>
        <li>Avoid excessive cross-posting. Choose the most relevant community for your post.</li>
        <li>Keep discussions civil and on-topic.</li>
        </ul>
    </div>

    <div class="rule-category">
        <h2>Voting and Engagement</h2>
        <ul>
        <li>Do not engage in vote manipulation or brigading.</li>
        <li>Contribute constructively to discussions through thoughtful comments.</li>
        <li>Report content that violates these rules.</li>
        </ul>
    </div>

    <div class="rule-category">
        <h2>Consequences</h2>
        <ul>
        <li>Violations of these rules may result in permanent bans.</li>
        <li>Moderators reserve the right to remove content that violates these rules or disrupts the community.</li>
        </ul>
    </div>

    <p class="contact">If you have any questions about these rules, please contact the moderators.</p>
</div>

